/// Look up an embedded theme by name (case-sensitive, like Ghostty).
pub fn get(name: []const u8) ?[]const u8 {
    for (&entries) |*entry| {
        if (std.mem.eql(u8, entry.name, name)) return entry.data;
    }
    return null;
}
